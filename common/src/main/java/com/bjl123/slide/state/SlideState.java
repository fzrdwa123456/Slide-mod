package com.bjl123.slide.state;

/**
 * 滑铲状态机状态枚举
 * 用于清晰地管理玩家滑铲过程中的各种状态
 */
public enum SlideState {
    /**
     * 空闲状态：玩家没有在滑铲
     * 可以转换到: SLIDING (当触发滑铲时)
     */
    IDLE,
    
    /**
     * 滑铲中：玩家正在滑铲
     * 可以转换到: 
     * - ENDING_TO_STAND (滑铲结束，空间足够站立且没按潜行键)
     * - ENDING_TO_CROUCH (滑铲结束，空间不足或按着潜行键)
     */
    SLIDING,
    
    /**
     * 结束中-站立：滑铲刚结束，过渡到站立姿势
     * 持续几帧以确保姿势稳定
     * 可以转换到: IDLE (过渡完成)
     */
    ENDING_TO_STAND,
    
    /**
     * 结束中-蹲伏：滑铲刚结束，过渡到蹲伏姿势
     * 当头顶有障碍物或玩家按着潜行键时进入此状态
     * 可以转换到: 
     * - IDLE (障碍物消失且没按潜行键)
     * - 保持此状态 (障碍物存在或按着潜行键)
     */
    ENDING_TO_CROUCH
}
